#pragma once
using namespace std;
#include <vector>
#include <string>

class Solution {
public:
    vector<int> findAnagrams(string s, string p) {
        int sLen = s.size(), pLen = p.size();
        if (sLen < pLen)
        {
            return vector<int>();
        }

        vector<int> res;
        vector<int> sCount(26);
        vector<int> pCount(26);
        for (int i = 0; i < pLen; i++)
        {
            ++sCount[s[i] - 'a'];
            ++pCount[p[i] - 'a'];
        }
        if (sCount == pCount)
        {
            res.emplace_back(0);
        }
        for (int i = 0; i < sLen - pLen; i++)
        {
            --sCount[s[i] - 'a'];
            ++sCount[s[i + pLen] - 'a'];
            if (sCount == pCount)
            {
                res.emplace_back(i + 1);
            }
        }
        return res;
    }
};